<h2><u>Chronology</u></h2>
<a href="#top">Go to top</a>
	<p>The history of Earth concerns the development of planet Earth from its formation to the present day.[1][2] Nearly all branches of natural science have contributed to understanding of the main events of Earth's past, characterized by constant geological change and biological evolution.
	</p>
<a id="for"></a>
<h4><u>formation</u></h4>
	<p>The oldest material found in the Solar System is dated to 4.5672±0.0006 billion years ago (Bya).[35] By 4.54±0.04 Bya[36] the primordial Earth had formed. The bodies in the Solar System formed and evolved with the Sun. In theory, a solar nebula partitions a volume out of a molecular cloud by gravitational collapse, which begins to spin and flatten into a circumstellar disk, and then the planets grow out of that disk with the Sun.</p>
