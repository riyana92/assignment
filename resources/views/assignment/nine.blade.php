<h4><u>shape</u></h4>
	<p>The shape of Earth is nearly spherical. There is a small flattening at the poles and bulging around the equator due to Earth's rotation.[82] To second order, the Earth is approximately an oblate spheroid, whose equatorial diameter is 43 kilometres (27 mi) larger than the pole-to-pole diameter,[83] although the variation is less than 1% of the average radius of the Earth.
</p>	
